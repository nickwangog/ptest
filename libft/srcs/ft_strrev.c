#include "libft.h"

char	*ft_strrev(char *str)
{
	int		i;
	int		a;
	char	t;

	a = 0;
	while (str[a] != '\0')
		a++;
	i = -1;
	while (i++ < a--)
	{
		t = str[i];
		str[i] = str[a];
		str[a] = t;
	}
	return (str);
}
